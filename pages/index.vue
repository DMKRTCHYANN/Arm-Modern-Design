onMounted(() => {
  const rows = divRef.value.querySelectorAll('.row');

  const handleScroll = () => {
    if (!divRef.value) return;
    const rect = divRef.value.getBoundingClientRect();
    const windowHeight = window.innerHeight;
    if (rect.top <= windowHeight && rect.bottom >= 0) {
      const scrollProgress = (windowHeight - rect.top) / (rect.height + windowHeight);
      rows.forEach((row, index) => {
        const direction = row.dataset.scrollDirection === 'right' ? 1 : -1;
        const offset = scrollProgress * 50 * direction * (3 + index * 0.2);
        row.style.transform = `translateX(${offset}px)`;
      });
    } else {
      rows.forEach((row) => {
        row.style.transform = 'translateX(0)';
      });
    }
  };

  window.addEventListener('scroll', handleScroll);

  onUnmounted(() => {
    window.removeEventListener('scroll', handleScroll);
  });
});
